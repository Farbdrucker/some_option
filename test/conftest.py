def add(a: int, b: int) -> int:
    return a + b


def sub(a: int, b: int) -> int:
    return a - b


def mul(a: int, b: int) -> int:
    return a * b


def div(a: int, b: int) -> int:
    return a / b


def mod(a: int, b: int) -> int:
    return a % b


def pow(a: int, b: int) -> int:
    return a**b


def neg(a: int) -> int:
    return -a


def pos(a: int) -> int:
    return +a


def abs_(a: int) -> int:
    return abs(a)
